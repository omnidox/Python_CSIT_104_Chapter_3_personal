number = 6  # This variable holds the number of burritos
amount = 32  # This variable holds the total cost in dollars

# __________________________________________________________________

print()  # Print a blank line for spacing

# Use an f-string to print the message showing the number of burritos and their total cost

print("f-string format:")
print(f'')
print('')
print(f'{number} burritos cost ${amount}')  # Output: '6 burritos cost $32'

print()  # Print another blank line for spacing

# __________________________________________________________________


# Use regular string concatenation to print the same message

print("Regular String Concatenation:")
print(str(number) + ' burritos cost $' + str(amount))  # Output: '6 burritos cost $32'

print()  # Print another blank line for spacing