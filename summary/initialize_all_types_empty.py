

print()
# Initializing an empty string
empty_string = ""  # An empty string contains no characters

# Initializing an empty list
empty_list = []  # An empty list is a mutable container with no elements

# Initializing an empty tuple
empty_tuple = ()  # An empty tuple is an immutable container with no elements

# Initializing an empty set
empty_set = set()  # An empty set is a mutable container with no elements; note: you cannot use {} as it creates an empty dictionary

# Initializing an empty dictionary
empty_dict = {}  # An empty dictionary is a container with no key-value pairs

# Printing all initialized empty data types
print("Empty string:", empty_string)
print()
print("Empty list:", empty_list)
print()
print("Empty tuple:", empty_tuple)
print()
print("Empty set:", empty_set)
print()
print("Empty dictionary:", empty_dict)
print()
